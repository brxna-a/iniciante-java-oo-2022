
public class MentorOrganizador {
	private Mentores mentor;
	private Organizadores organizador;
	private Integrantes integrantes;
	private CriarEvento evento;
	
	public Mentores getMentor() {
		return mentor;
	}
	public void setMentor(Mentores mentor) {
		this.mentor = mentor;
	}
	public Organizadores getOrganizador() {
		return organizador;
	}
	public void setOrganizador(Organizadores organizador) {
		this.organizador = organizador;
	}
	public Integrantes getIntegrantes() {
		return integrantes;
	}
	public void setIntegrantes(Integrantes integrantes) {
		this.integrantes = integrantes;
	}
	public CriarEvento getEvento() {
		return evento;
	}
	public void setEvento(CriarEvento evento) {
		this.evento = evento;
	}
	
	
}
