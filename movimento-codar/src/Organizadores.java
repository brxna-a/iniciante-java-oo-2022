
public class Organizadores {
	private String responderPerguntas;
	private Integrantes integrantes;
	private CriarEvento evento;
	
	
	public String getResponderPerguntas() {
		return responderPerguntas;
	}
	public void setResponderPerguntas(String responderPerguntas) {
		this.responderPerguntas = responderPerguntas;
	}
	public CriarEvento getEvento() {
		return evento;
	}
	public void setEvento(CriarEvento evento) {
		this.evento = evento;
	}
	public Integrantes getIntegrantes() {
		return integrantes;
	}
	public void setIntegrantes(Integrantes integrantes) {
		this.integrantes = integrantes;
	}
}
